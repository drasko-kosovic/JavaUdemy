package com.company.composition;

public class Dimenzije {
    private int sirina;
    private int visina;

    public Dimenzije(int sirina, int visina) {
        this.sirina = sirina;
        this.visina = visina;
    }

    public int getSirina() {
        return sirina;
    }

    public int getVisina() {
        return visina;
    }
}
